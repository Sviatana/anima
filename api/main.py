        reply = "–î–∞–≤–∞–π –æ—Å—Ç–∞–≤–∏–º —á—É–≤—Å—Ç–≤–∏—Ç–µ–ª—å–Ω—ã–µ —Ç–µ–º—ã –∑–∞ —Ä–∞–º–∫–∞–º–∏. –û —á—ë–º —Ç–µ–±–µ –≤–∞–∂–Ω–µ–µ –ø–æ–≥–æ–≤–æ—Ä–∏—Ç—å —Å–µ–π—á–∞—Å?"
