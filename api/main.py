    p = pr[0] if pr else {"ei":0.5,"sn":0.5,"tf":0.5,"jp":0.5}
    st = comms_style(p)
    t = (user_text or "").strip()

    # –≤–∫–ª—é—á–µ–Ω–∏–µ —é–º–æ—Ä–∞ –ø–æ –∫–æ–º–∞–Ω–¥–µ –æ–±—ã—á–Ω—ã–º —Ç–µ–∫—Å—Ç–æ–º
