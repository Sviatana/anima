def detect_emotion(t: str) -> str:
    tl = (t or "").lower()
    if re.search(r"—É—Å—Ç–∞–ª|–Ω–∞–ø—Ä—è–∂|—Ç—Ä–µ–≤–æ–∂|—Å—Ç—Ä–∞—à|–∑–ª—é—Å—å|–∑–ª–æ—Å—Ç—å|—Ä–∞–∑–¥—Ä–∞–∂|–≥—Ä—É—Å—Ç–Ω|–ø–ª–æ—Ö–æ|–ø–∞–Ω–∏–∫",tl): return "tense"
